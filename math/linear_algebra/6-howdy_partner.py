#!/usr/bin/env python3
"""Module that concatenates two arrays."""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays (lists) and returns a new list.

    Args:
        arr1 (list of int/float): First array
        arr2 (list of int/float): Second array

    Returns:
        list: New list containing elements from arr1 followed by arr2
    """
    return arr1 + arr2
